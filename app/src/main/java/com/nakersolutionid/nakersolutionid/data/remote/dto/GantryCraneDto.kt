// --- Generic Inspection Result DTO (for Visual Inspection) ---
// Note: This matches the pattern of [field]Memenuhi, [field]TidakMemenuhi, [field]Result
// However, the overall structure of VisualInspection is flat,
// so individual fields are mapped directly rather than nested objects.
// Keeping this common Result DTO for `status` if it were consistently used in that form.
// But based on the provided JSON for Gantry Crane Visual Inspection,
// it is structured with separate boolean and string fields.
// Therefore, the GantryCraneReportInspectionResult as a standalone is not directly applicable
// to the `VisualInspection` section's internal structure; each `...Result` field
// within `VisualInspection` just takes a String.
// The booleans `...Memenuhi` and `...TidakMemenuhi` are also direct fields.
// This `GantryCraneReportInspectionResult` will serve for generic pass/fail status
// where `result` is a string description and `status` is a boolean, if such a pattern emerges.
// In Gantry Crane's Visual Inspection, the pattern is:
// "anchorBoltscorrosionMemenuhi": true,
// "anchorBoltscorrosionTidakMemenuhi": false,
// "anchorBoltscorrosionResult": "Kondisi Baik",
// This means each of these is a direct field, not nested within a `GantryCraneReportInspectionResult` object.
// So, this generic Result DTO is actually not used *within* the Gantry Crane Visual Inspection as it was for Forklift/Elevator.
// It could be used for other parts if they had `{"result": "...", "status": true/false}`.
// I will keep it in case it's a general utility DTO intended for consistency across reports,
// but it's not applied to the VisualInspection section itself based on the current JSON structure.
data class GantryCraneReportInspectionResult(
    @SerializedName("result") val result: String = "",
    @SerializedName("status") val status: Boolean? = null
)